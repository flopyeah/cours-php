PHP - Les dates
Pour tous les exercices, vous pouvez utiliser le site http://php.net
https://www.php.net/manual/fr/ref.datetime.php

Exercice 1
Afficher la date courante en respectant la forme jj/mm/aaaa (ex : 16/05/2019)

Exercice 2
Afficher la date courante en respectant la forme jj-mm-aa (ex : 16-05-19)

Exercice 3
Afficher le timestamp du jour.

Exercice 4
Afficher la date courante avec le jour de la semaine et le mois en toutes lettres (ex : mardi 2 août 2019)
